package shopping.common.model;

public class Combo05 {
	private String id; // 아이디
	private int cnt; // 주문 건수
	
	
	public Combo05() {
	}


	@Override
	public String toString() {
		return "Combo05 [id=" + id + ", cnt=" + cnt + "]";
	}


	public String getId() {
		return id;
	}


	public void setId(String id) {
		this.id = id;
	}


	public int getCnt() {
		return cnt;
	}


	public void setCnt(int cnt) {
		this.cnt = cnt;
	}


	
	
	
	
}
